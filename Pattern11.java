import java.util.*;
class Pattern11
{
     public static void main(String args[])
     {
       Scanner sc=new Scanner(System.in);
       char i,j;
      
       for(i='E';i>='A';--i)
       {
         for(j='E';j>='A';--j)
         {
          System.out.print(j);
          }
          System.out.println(" ");
       }
    }
}