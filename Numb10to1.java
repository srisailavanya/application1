import java.util.*;
class Numb10to1
{ 
      public static void main(String args[])
      {
           Scanner sc=new Scanner(System.in);
           int n;
           System.out.println("enter the value of n");
           n=sc.nextInt();
           
           n=1;
           while(n>=1)
                {
                      System.out.print(n+" ");
                      n--;
                }
        }
}